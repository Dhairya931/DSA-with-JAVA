package Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a=451.23584f;
        System.out.printf("Formatted number is: %.2f",a);
        System.out.println("");
        System.out.printf("Pie: %.3f",Math.PI);
        System.out.println("");
        System.out.printf("Hi my name is %s and I am a %s","Dhairya","student");
    }
}
